    NO_EDITORA    VARCHAR(30) NOT NULL COLLATE PT_BR,
    NR_EDITORA    VARCHAR(15) NOT NULL COLLATE PT_BR,
    DT_CRIACAO    TIMESTAMP DEFAULT 'NOW' NOT NULL,
